#!/usr/bin/bash


mkdir -p stats
(
echo -e "\n\n"\
"==============================================================================="\
"\n[ NeoFETCH ]\n"\
"==============================================================================="
neofetch 
echo -e \
"_______________________________________________________________________________"

echo -e "\n\n"\
"==============================================================================="\
"\n[ free -h ]\n"\
"==============================================================================="
free -h 
echo -e \
"_______________________________________________________________________________"

echo -e "\n\n"\
"==============================================================================="\
"\n[ df -h ]\n"\
"==============================================================================="
df -h 
echo -e \
"_______________________________________________________________________________"

) | tee stats/$1_summary.txt

sudo dpkg-query -Wf '${Package};${Installed-Size};${Depends};${Recommends};${Suggests}\n' \
> stats/$1_installed_packages.txt
